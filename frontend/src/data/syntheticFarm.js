export function generateLiveSensorReading(zone) {
  const base = zone.sensors
  const jitter = (val, range) => Math.round((val + (Math.random() - 0.5) * range) * 10) / 10
  return {
    timestamp: Date.now(),
    moisture: jitter(base.moisture, 4),
    temperature: jitter(base.temperature, 2),
    ph: jitter(base.ph, 0.3),
  }
}
